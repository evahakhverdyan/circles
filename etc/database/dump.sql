DROP TABLE IF EXISTS `users`;
CREATE TABLE `users` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `uuid` VARCHAR(36) NULL DEFAULT NULL,
  `dateRegistered` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `name` VARCHAR(255) NULL DEFAULT NULL,
  `email` VARCHAR(255) NULL DEFAULT NULL,
  `birthday` TIMESTAMP NULL DEFAULT NULL,
  `sex` VARCHAR(6) NULL DEFAULT NULL,
  `status` VARCHAR(11) NULL DEFAULT NULL,
  `aim` VARCHAR(6) NULL DEFAULT NULL,
  `education` VARCHAR(6) NULL DEFAULT NULL,
  `phone` INT(10) NULL DEFAULT NULL,
  `about` TEXT NULL DEFAULT NULL,
  `needSex` VARCHAR(6) NULL DEFAULT NULL,
  `needStatus` VARCHAR(11) NULL DEFAULT NULL,
  `needAim` VARCHAR(6) NULL DEFAULT NULL,
  `needEducation` VARCHAR(6) NULL DEFAULT NULL,
  PRIMARY KEY (`id`),
  UNIQUE INDEX `email` (`email` ASC),
  UNIQUE INDEX `uuid` (`uuid` ASC)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;